#include "ft_printf.h"

int	ft_print_d(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
		len++;
	}
	if (n > 9)
		len += ft_print_d(n / 10);
	len += ft_print_c((n % 10) + '0');
	return (len);
}
